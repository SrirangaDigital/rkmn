						<b>Guru:</b> How do you see (What is that light/power which helps you see?). 
						<b>Sishya:</b> I see with the help of sunlight. 
						<b>Guru:</b> How do you see in the night?. 
						<b>Sishya:</b> I see with the help of a lamp. 
						<b>Guru:</b> Let that be so. How do you see the light? How do you see (your visualizations) even before you open your eyes?. 
						<b>Sishya:</b> It is with my intellect. 
						<b>Guru:</b> What helps you see (know) that intellect?. 
						<b>Sishya:</b> This is me (me as pure consciousness). 
						<b>Guru:</b> (Indeed) You are that supreme light. 
						<b>Sishya:</b> I realize that I am. <br />&nbsp;
